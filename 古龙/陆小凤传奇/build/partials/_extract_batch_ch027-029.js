#!/usr/bin/env node
'use strict';

const fs = require('fs');
const path = require('path');

const novelDir = path.resolve(__dirname, '../..');
const buildDir = path.join(novelDir, 'build');
const sourceIndex = JSON.parse(fs.readFileSync(path.join(buildDir, 'source-index.json'), 'utf8'));
const winMap = new Map(sourceIndex.windows.map(w => [w.id, w]));

function norm(t) {
  return String(t ?? '').replace(/\r\n?/g, '\n').normalize('NFKC').replace(/\s+/gu, '');
}

function make(windowId, chapter, seq, category, name, ls, le, text) {
  return {
    candidate_id: `cand_${windowId}_${String(seq).padStart(4, '0')}`,
    category_hint: category,
    name,
    chapter,
    source_ref: { line_start: ls, line_end: le, text },
    discovery_pass: 'named-inventory',
    window_id: windowId
  };
}

function validate(c) {
  const w = winMap.get(c.window_id);
  if (!w) return `no window ${c.window_id}`;
  if (!norm(w.text).includes(norm(c.source_ref.text))) return 'not found in window';
  return null;
}

