function loadWindow(id) {
  const raw = fs.readFileSync(path.join(WIN_DIR, `${id}.txt`), 'utf8');
  const map = new Map();
  const bodyLines = [];
  for (const line of raw.split(/\r?\n/)) {
    const i = line.indexOf('|');
    if (i < 0) continue;
    const n = Number(line.slice(0, i));
    const t = line.slice(i + 1);
    if (Number.isFinite(n)) map.set(n, t);
    bodyLines.push(t);
  }
  return { map, body: bodyLines.join('\n'), rawBody: bodyLines };
}

function findText(win, text) {
  if (!win.body.includes(text)) {
    throw new Error(`source text not found:\n${text.slice(0, 80)}`);
  }
  // find line_start/end by scanning map
  let start = null, end = null;
  const keys = [...win.map.keys()].sort((a, b) => a - b);
  for (const k of keys) {
    if (win.map.get(k).includes(text) || text.includes(win.map.get(k)) && win.map.get(k).length > 10) {
      if (start == null) start = k;
      end = k;
    }
  }
  // better: locate substring position and map to lines
  const body = win.body;
  const pos = body.indexOf(text);
  if (pos < 0) throw new Error('not found');
  let acc = 0;
  start = null;
  end = null;
  for (const k of keys) {
    const line = win.map.get(k);
    const lineStart = acc;
    // account for join with \n
    const lineEnd = acc + line.length;
    if (pos < lineEnd && pos + text.length > lineStart) {
      if (start == null) start = k;
      end = k;
    }
    acc = lineEnd + 1; // \n
  }
  if (start == null) {
    // fallback: search any line containing first 20 chars
    const snip = text.slice(0, 20);
    for (const k of keys) {
      if (win.map.get(k).includes(snip)) {
        start = end = k;
        break;
      }
    }
  }
  if (start == null) throw new Error(`cannot locate lines for: ${text.slice(0, 40)}`);
  return { line_start: start, line_end: end, text };
}

function named(id, seq, category, name, text, win) {
  const ref = findText(win, text);
  return {
    candidate_id: `cand_${id}_${String(seq).padStart(4, '0')}`,
    category_hint: category,
    name,
    chapter: CHAPTER,
    source_ref: ref,
    discovery_pass: 'named-inventory',
    window_id: id,
  };
}

function event(id, seq, name, text, win, level = 'main') {
  const ref = findText(win, text);
  return {
    candidate_id: `cand_${id}_${String(seq).padStart(4, '0')}`,
    category_hint: 'event',
    name,
    chapter: CHAPTER,
    source_ref: ref,
    discovery_pass: 'event-dialogue',
    window_id: id,
    event_level_hint: level,
  };
}

function dialogue(id, seq, name, speaker, text, ctx, win, type, reason, traits) {
  const ref = findText(win, text);
  const cref = findText(win, ctx);
  const o = {
    candidate_id: `cand_${id}_${String(seq).padStart(4, '0')}`,
    category_hint: 'dialogue',
    name,
    chapter: CHAPTER,
    source_ref: ref,
    discovery_pass: 'event-dialogue',
    window_id: id,
    speaker_name: speaker,
    selection_type_hint: type,
    selection_reason: reason,
    context_source_ref: cref,
  };
  if (traits) o.trait_tags = traits;
  return o;
}

function summary(id, seq, title, summaryText, keyEvents, text, win) {
  const ref = findText(win, text);
  return {
    candidate_id: `cand_${id}_${String(seq).padStart(4, '0')}`,
    category_hint: 'chapter_summary',
    name: title,
    chapter: CHAPTER,
    source_ref: ref,
    discovery_pass: 'event-dialogue',
    window_id: id,
    summary: summaryText,
    key_events: keyEvents,
  };
}

